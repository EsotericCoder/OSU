#include "LineSegment.hpp"

/***********************************************************************************
**Description: This constructor takes two Points as a parameters and sets them to.
***********************************************************************************/
LineSegment::LineSegment(Point first, Point second)
{   p1 = first;
    p2 = second;
}
    
/***********************************************************************************
**Description: Sets End 1
**
***********************************************************************************/
void LineSegment::setEnd1(Point one)
{   p1 = one; }
    
/***********************************************************************************
**Description: Sets End 2
**
***********************************************************************************/
void LineSegment::setEnd2(Point two)
{   p2 = two; }
    
/***********************************************************************************
**Description: Gets End 1
**
***********************************************************************************/
Point LineSegment::getEnd1()
{   return p1; }
    
/***********************************************************************************
**Description: Gets End 2
**
***********************************************************************************/
Point LineSegment::getEnd2()
{   return p2; }
    
/***********************************************************************************
**Description: Calculates the distance from one point to another
**
***********************************************************************************/
double LineSegment:: length()
{
    double length = p1.distanceTo(p2);
    return length;
}
    
/***********************************************************************************
**Description: Calculates the slope
**
***********************************************************************************/
double LineSegment:: slope()
{
    double slope = (p2.getYCoord() - p1.getYCoord())/(p2.getXCoord() - p1.getXCoord());
    return slope;
}

